    unsigned long int i, j, x, flag = 0;    /* unsigned long int型の変数i, j, x, flagを宣言し, flagを0で初期化する. */
    x = sqrt(p);    /* pの平方根を計算し, その整数部分の値をxに代入する. */

    /* xの探索範囲がsqrt(p)以下でよいことは初等整数論の結果から自明である. */
